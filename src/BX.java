public class BX {
    double B;
    double X;
    double Bx;
    public BX(double B, double X){
        this.B = B;
        this.X = X;
    }

    public double getB() {
        return B;
    }

    public void setB(double b) {
        B = b;
    }

    public double getX() {
        return X;
    }

    public void setX(double x) {
        X = x;
    }

    public double getBx() {
        return Bx = B*X;
    }

    public void setBx(double bx) {
        Bx = bx;
    }
}
